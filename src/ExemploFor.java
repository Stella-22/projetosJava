public class ExemploFor {
    public static void main(String[]args)

    {
        System.out.println("Contagem de 1 a 5:");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        System.out.println("Contagem regressiva de 5 a 1:");
        for (int j = 5; j >= 1; j--) {
            System.out.println(j);
            }
        }
    }

